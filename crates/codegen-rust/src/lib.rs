//! Deterministic Rust source generation for backend-neutral ferrule programs.

#![forbid(unsafe_code)]

use std::collections::{BTreeMap, BTreeSet};
use std::fmt;

use codegen::{
    ArtifactPath, ArtifactPathError, ArtifactSet, ArtifactSetError, Binding, Expression,
    GeneratedFile, Program, TargetScope,
};
use ir::{ScalarType, Value};
use mapping::NodeId;

/// How a generated Cargo project locates ferrule's small Rust runtime.
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum RuntimeDependency {
    Path(String),
    Version(String),
    Git {
        repository: String,
        revision: String,
    },
}

/// Rust project generation settings.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct Options {
    pub package_name: String,
    pub runtime_dependency: RuntimeDependency,
}

/// Failure to render a complete Rust project.
#[derive(Debug)]
pub enum EmitError {
    InvalidPackageName(String),
    DuplicateNode { node: NodeId },
    MissingExpression { scope: String, node: NodeId },
    InvalidDuplicateBinding { scope: String, binding: usize },
    NonFiniteFloat { node: NodeId },
    ArtifactPath(ArtifactPathError),
    ArtifactSet(ArtifactSetError),
}

impl fmt::Display for EmitError {
    fn fmt(&self, formatter: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            Self::InvalidPackageName(name) => {
                write!(formatter, "invalid generated Rust package name {name:?}")
            }
            Self::DuplicateNode { node } => {
                write!(formatter, "compiled mapping contains duplicate node {node}")
            }
            Self::MissingExpression { scope, node } => {
                write!(
                    formatter,
                    "scope {scope} references missing expression {node}"
                )
            }
            Self::InvalidDuplicateBinding { scope, binding } => write!(
                formatter,
                "scope {scope} binding {binding} duplicates a non-repeating or incompatible target"
            ),
            Self::NonFiniteFloat { node } => {
                write!(
                    formatter,
                    "graph node {node} contains a non-finite float literal"
                )
            }
            Self::ArtifactPath(error) => error.fmt(formatter),
            Self::ArtifactSet(error) => error.fmt(formatter),
        }
    }
}

impl std::error::Error for EmitError {}

impl From<ArtifactPathError> for EmitError {
    fn from(error: ArtifactPathError) -> Self {
        Self::ArtifactPath(error)
    }
}

impl From<ArtifactSetError> for EmitError {
    fn from(error: ArtifactSetError) -> Self {
        Self::ArtifactSet(error)
    }
}

/// Emits one buildable Rust library project.
pub fn emit(program: &Program, options: &Options) -> Result<ArtifactSet, EmitError> {
    if !valid_package_name(&options.package_name) {
        return Err(EmitError::InvalidPackageName(options.package_name.clone()));
    }
    let manifest = render_manifest(options);
    let source = render_source(program)?;
    ArtifactSet::new([
        GeneratedFile::new(ArtifactPath::new("Cargo.toml")?, manifest),
        GeneratedFile::new(ArtifactPath::new("src/lib.rs")?, source),
    ])
    .map_err(EmitError::from)
}

fn valid_package_name(name: &str) -> bool {
    !name.is_empty()
        && name
            .bytes()
            .all(|byte| byte.is_ascii_alphanumeric() || matches!(byte, b'-' | b'_'))
        && name
            .bytes()
            .next()
            .is_some_and(|byte| byte.is_ascii_alphabetic())
}

fn render_manifest(options: &Options) -> String {
    let dependency = match &options.runtime_dependency {
        RuntimeDependency::Path(path) => format!("path = {}", toml_string(path)),
        RuntimeDependency::Version(version) => format!("version = {}", toml_string(version)),
        RuntimeDependency::Git {
            repository,
            revision,
        } => format!(
            "git = {}, rev = {}",
            toml_string(repository),
            toml_string(revision)
        ),
    };
    format!(
        "[package]\nname = {}\nversion = \"0.1.0\"\nedition = \"2024\"\npublish = false\n\n[dependencies]\ncodegen-runtime = {{ {dependency} }}\n",
        toml_string(&options.package_name)
    )
}

fn render_source(program: &Program) -> Result<String, EmitError> {
    let mut source = String::from(
        "// Generated by ferrule. Do not edit.\n\
         use codegen_runtime::{\n\
             Instance, ScalarType, SourcePathError, Value, adapt_target_value, field, group,\n\
             repeated, resolve_scalar, scalar,\n\
         };\n\n",
    );
    source.push_str(
        "pub fn execute(source: &Instance) -> Result<Instance, SourcePathError> {\n    scope_root(source)\n}\n\n",
    );

    let mut expression_ids = BTreeSet::new();
    for node in &program.expressions {
        if !expression_ids.insert(node.id) {
            return Err(EmitError::DuplicateNode { node: node.id });
        }
        source.push_str(&render_expression(node.id, &node.expression)?);
    }

    let mut scopes = Vec::new();
    collect_scopes(&program.root, "scope_root".to_string(), &mut scopes);
    for (name, scope, child_names) in scopes {
        source.push_str(&render_scope(&name, scope, &child_names, &expression_ids)?);
    }
    Ok(source)
}

fn render_expression(id: NodeId, expression: &Expression) -> Result<String, EmitError> {
    let body = match expression {
        Expression::SourceField { path } => {
            let path = path
                .iter()
                .map(|segment| rust_string(segment))
                .collect::<Vec<_>>()
                .join(", ");
            format!("resolve_scalar(source, &[{path}])")
        }
        Expression::Const { value } => {
            format!("Ok({})", rust_value(id, value)?)
        }
    };
    Ok(format!(
        "fn expression_{id}(source: &Instance) -> Result<Value, SourcePathError> {{\n    let _ = source;\n    {body}\n}}\n\n"
    ))
}

fn collect_scopes<'a>(
    scope: &'a TargetScope,
    name: String,
    output: &mut Vec<(String, &'a TargetScope, Vec<String>)>,
) {
    let child_names = scope
        .children
        .iter()
        .enumerate()
        .map(|(index, _)| format!("{name}_{index}"))
        .collect::<Vec<_>>();
    output.push((name, scope, child_names.clone()));
    for (child, child_name) in scope.children.iter().zip(child_names) {
        collect_scopes(child, child_name, output);
    }
}

fn render_scope(
    name: &str,
    scope: &TargetScope,
    child_names: &[String],
    expression_ids: &BTreeSet<NodeId>,
) -> Result<String, EmitError> {
    let mut output =
        format!("fn {name}(source: &Instance) -> Result<Instance, SourcePathError> {{\n");
    for (index, binding) in scope.bindings.iter().enumerate() {
        if !expression_ids.contains(&binding.expression) {
            return Err(EmitError::MissingExpression {
                scope: name.to_string(),
                node: binding.expression,
            });
        }
        output.push_str(&format!(
            "    let value_{index} = adapt_target_value(expression_{}(source)?, ScalarType::{});\n",
            binding.expression,
            scalar_type_name(binding.target_type)
        ));
    }

    let groups = binding_groups(name, &scope.bindings)?;
    for (group_index, group) in groups.iter().enumerate() {
        if !group.repeating {
            continue;
        }
        output.push_str(&format!(
            "    let mut repeated_{group_index} = Vec::new();\n"
        ));
        for binding_index in &group.binding_indices {
            output.push_str(&format!(
                "    if value_{binding_index} != Value::Null {{\n        repeated_{group_index}.push(scalar(value_{binding_index}));\n    }}\n"
            ));
        }
    }

    let mut fields = Vec::new();
    for (group_index, binding_group) in groups.iter().enumerate() {
        let name = rust_string(&binding_group.target_field);
        if binding_group.repeating {
            fields.push(format!("field({name}, repeated(repeated_{group_index}))"));
        } else {
            let index = binding_group.binding_indices[0];
            fields.push(format!("field({name}, scalar(value_{index}))"));
        }
    }
    for (child, child_name) in scope.children.iter().zip(child_names) {
        fields.push(format!(
            "field({}, {child_name}(source)?)",
            rust_string(&child.target_field)
        ));
    }
    if fields.is_empty() {
        output.push_str("    let output = group(Vec::new());\n");
    } else {
        output.push_str("    let output = group([\n");
        for field in fields {
            output.push_str(&format!("        {field},\n"));
        }
        output.push_str("    ]);\n");
    }
    if scope.repeating {
        output.push_str("    Ok(repeated([output]))\n");
    } else {
        output.push_str("    Ok(output)\n");
    }
    output.push_str("}\n\n");
    Ok(output)
}

struct BindingGroup {
    target_field: String,
    target_type: ScalarType,
    repeating: bool,
    binding_indices: Vec<usize>,
}

fn binding_groups(scope: &str, bindings: &[Binding]) -> Result<Vec<BindingGroup>, EmitError> {
    let mut groups = Vec::<BindingGroup>::new();
    let mut indices = BTreeMap::<&str, usize>::new();
    for (binding_index, binding) in bindings.iter().enumerate() {
        if let Some(index) = indices.get(binding.target_field.as_str()).copied() {
            let group = &mut groups[index];
            if !group.repeating || !binding.repeating || group.target_type != binding.target_type {
                return Err(EmitError::InvalidDuplicateBinding {
                    scope: scope.to_string(),
                    binding: binding_index,
                });
            }
            group.binding_indices.push(binding_index);
        } else {
            indices.insert(&binding.target_field, groups.len());
            groups.push(BindingGroup {
                target_field: binding.target_field.clone(),
                target_type: binding.target_type,
                repeating: binding.repeating,
                binding_indices: vec![binding_index],
            });
        }
    }
    Ok(groups)
}

fn scalar_type_name(value: ScalarType) -> &'static str {
    match value {
        ScalarType::String => "String",
        ScalarType::Int => "Int",
        ScalarType::Float => "Float",
        ScalarType::Bool => "Bool",
    }
}

fn rust_value(node: NodeId, value: &Value) -> Result<String, EmitError> {
    Ok(match value {
        Value::Null => "Value::Null".to_string(),
        Value::XmlNil(_) => "Value::xml_nil()".to_string(),
        Value::Bool(value) => format!("Value::Bool({value})"),
        Value::Int(i64::MIN) => "Value::Int(i64::MIN)".to_string(),
        Value::Int(value) => format!("Value::Int({value}_i64)"),
        Value::Float(value) if value.is_finite() => {
            format!("Value::Float({value:?}_f64)")
        }
        Value::Float(_) => return Err(EmitError::NonFiniteFloat { node }),
        Value::String(value) => format!("Value::String({}.to_string())", rust_string(value)),
    })
}

fn rust_string(value: &str) -> String {
    let escaped = value
        .chars()
        .flat_map(char::escape_default)
        .collect::<String>();
    format!("\"{escaped}\"")
}

fn toml_string(value: &str) -> String {
    let mut output = String::from("\"");
    for character in value.chars() {
        match character {
            '\u{8}' => output.push_str("\\b"),
            '\t' => output.push_str("\\t"),
            '\n' => output.push_str("\\n"),
            '\u{c}' => output.push_str("\\f"),
            '\r' => output.push_str("\\r"),
            '"' => output.push_str("\\\""),
            '\\' => output.push_str("\\\\"),
            character if character.is_ascii() && !character.is_control() => {
                output.push(character);
            }
            character if (character as u32) <= 0xffff => {
                output.push_str(&format!("\\u{:04X}", character as u32));
            }
            character => output.push_str(&format!("\\U{:08X}", character as u32)),
        }
    }
    output.push('"');
    output
}

#[cfg(test)]
mod tests {
    use std::fs;
    use std::path::{Path, PathBuf};
    use std::process::Command;
    use std::time::{SystemTime, UNIX_EPOCH};

    use super::*;
    use codegen::{Binding, ExpressionNode};
    use ir::SchemaNode;

    fn program() -> Program {
        Program {
            source: SchemaNode::group(
                "Source",
                vec![SchemaNode::scalar("Name", ScalarType::String)],
            ),
            target: SchemaNode::group(
                "Target",
                vec![
                    SchemaNode::scalar("Copied", ScalarType::String),
                    SchemaNode::scalar("Numbers", ScalarType::Int).repeating(),
                    SchemaNode::group(
                        "Nested",
                        vec![SchemaNode::scalar("Constant", ScalarType::String)],
                    )
                    .repeating(),
                ],
            ),
            expressions: vec![
                ExpressionNode {
                    id: 1,
                    expression: Expression::SourceField {
                        path: vec!["Name".to_string()],
                    },
                },
                ExpressionNode {
                    id: 2,
                    expression: Expression::Const {
                        value: Value::Float(7.0),
                    },
                },
                ExpressionNode {
                    id: 3,
                    expression: Expression::Const { value: Value::Null },
                },
                ExpressionNode {
                    id: 4,
                    expression: Expression::Const {
                        value: Value::String("fixed".to_string()),
                    },
                },
            ],
            root: TargetScope {
                target_field: String::new(),
                repeating: false,
                bindings: vec![
                    Binding {
                        target_field: "Copied".to_string(),
                        expression: 1,
                        target_type: ScalarType::String,
                        repeating: false,
                    },
                    Binding {
                        target_field: "Numbers".to_string(),
                        expression: 2,
                        target_type: ScalarType::Int,
                        repeating: true,
                    },
                    Binding {
                        target_field: "Numbers".to_string(),
                        expression: 3,
                        target_type: ScalarType::Int,
                        repeating: true,
                    },
                ],
                children: vec![TargetScope {
                    target_field: "Nested".to_string(),
                    repeating: true,
                    bindings: vec![Binding {
                        target_field: "Constant".to_string(),
                        expression: 4,
                        target_type: ScalarType::String,
                        repeating: false,
                    }],
                    children: Vec::new(),
                }],
            },
        }
    }

    #[test]
    fn emits_deterministic_rust_project() {
        let options = Options {
            package_name: "sample-map".to_string(),
            runtime_dependency: RuntimeDependency::Path("../runtime".to_string()),
        };
        let first = emit(&program(), &options).unwrap();
        let second = emit(&program(), &options).unwrap();
        assert_eq!(first, second);
        assert_eq!(
            first
                .files()
                .iter()
                .map(|file| file.path.as_str())
                .collect::<Vec<_>>(),
            ["Cargo.toml", "src/lib.rs"]
        );
        let source = first
            .files()
            .iter()
            .find(|file| file.path.as_str() == "src/lib.rs")
            .and_then(|file| std::str::from_utf8(&file.contents).ok())
            .unwrap();
        assert!(source.contains("adapt_target_value(expression_2(source)?, ScalarType::Int)"));
        assert!(source.contains("repeated_1.push(scalar(value_1))"));
        assert!(source.contains("scope_root_0(source)?"));
    }

    #[test]
    fn rejects_invalid_package_names_and_non_finite_literals() {
        let options = Options {
            package_name: "not/a/package".to_string(),
            runtime_dependency: RuntimeDependency::Version("0.1.0".to_string()),
        };
        assert!(matches!(
            emit(&program(), &options),
            Err(EmitError::InvalidPackageName(_))
        ));

        let mut invalid = program();
        invalid.expressions[1].expression = Expression::Const {
            value: Value::Float(f64::NAN),
        };
        assert!(matches!(
            emit(
                &invalid,
                &Options {
                    package_name: "sample-map".to_string(),
                    runtime_dependency: RuntimeDependency::Version("0.1.0".to_string()),
                }
            ),
            Err(EmitError::NonFiniteFloat { node: 2 })
        ));
    }

    #[test]
    fn generated_project_builds_and_matches_the_static_mapping() {
        let runtime = Path::new(env!("CARGO_MANIFEST_DIR"))
            .parent()
            .map(|parent| parent.join("codegen-runtime"))
            .unwrap();
        let output = TempDir::new("rust_codegen");
        let artifacts = emit(
            &program(),
            &Options {
                package_name: "sample-map".to_string(),
                runtime_dependency: RuntimeDependency::Path(runtime.display().to_string()),
            },
        )
        .unwrap();
        write_artifacts(output.path(), &artifacts);
        fs::write(
            output.path().join("src/main.rs"),
            r#"use codegen_runtime::{Instance, Value, field, group, repeated, scalar};

fn main() {
    let source = group([field("Name", scalar(Value::String("Ada".to_string())))]);
    let actual = sample_map::execute(&source).unwrap();
    let expected = group([
        field("Copied", scalar(Value::String("Ada".to_string()))),
        field("Numbers", repeated([scalar(Value::Int(7))])),
        field("Nested", repeated([group([field(
            "Constant",
            scalar(Value::String("fixed".to_string())),
        )])])),
    ]);
    assert_eq!(actual, expected);
}
"#,
        )
        .unwrap();

        let result = Command::new("cargo")
            .args(["run", "--quiet"])
            .current_dir(output.path())
            .env("CARGO_TARGET_DIR", output.path().join("target"))
            .output()
            .unwrap();
        assert!(
            result.status.success(),
            "generated Rust project failed:\n{}\n{}",
            String::from_utf8_lossy(&result.stdout),
            String::from_utf8_lossy(&result.stderr)
        );
    }

    fn write_artifacts(directory: &Path, artifacts: &ArtifactSet) {
        for file in artifacts.files() {
            let path = directory.join(file.path.as_str());
            if let Some(parent) = path.parent() {
                fs::create_dir_all(parent).unwrap();
            }
            fs::write(path, &file.contents).unwrap();
        }
    }

    struct TempDir(PathBuf);

    impl TempDir {
        fn new(tag: &str) -> Self {
            let nonce = SystemTime::now()
                .duration_since(UNIX_EPOCH)
                .map(|duration| duration.as_nanos())
                .unwrap_or_default();
            let path = std::env::temp_dir().join(format!(
                "ferrule_{tag}_{}_{}",
                std::process::id(),
                nonce
            ));
            fs::create_dir_all(&path).unwrap();
            Self(path)
        }

        fn path(&self) -> &Path {
            &self.0
        }
    }

    impl Drop for TempDir {
        fn drop(&mut self) {
            let _ = fs::remove_dir_all(&self.0);
        }
    }
}
